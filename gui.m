function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
[file, path] = uigetfile('*');
handles.text2.String = '(Preparing audio...)';
score = Score([path, file]);
handles.Score = score;
handles.Score.getPlayer();
handles.text2.String = getBaseName(file);
axes(handles.axes1)
pages = handles.Score.getPages();
img = pages{1}.getImage();
[nr, nc] = size(img);
padding = 50;
cMin = 1; 
while ~any(img(:, cMin+padding))
    cMin = cMin + 1;
end
rMin = 1;
while ~any(img(rMin+padding, :))
    rMin = rMin + 1;
end
rMax = round(nr/2); 
while any(img(rMax, :))
    rMax = rMax + 1;
end
rMax = rMax + padding;
cMax = nc; 
while ~any(img(:, cMax-padding))
    cMax = cMax - 1;
end
imshow(~img(rMin:rMax, cMin:cMax));
guidata(hObject,handles)

